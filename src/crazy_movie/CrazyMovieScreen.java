    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
    int fila = Tabla.getSelectedRow();
    if(fila == -1) {
        JOptionPane.showMessageDialog(null, "No ha seleccionado una fila");
    } else {
        
        idc = Integer.parseInt((String) Tabla.getValueAt(fila,0) .toString());
        String nom = (String) Tabla.getValueAt(fila, 1);
        String gen = (String) Tabla.getValueAt(fila, 2);
        String dur = (String) Tabla.getValueAt(fila, 3);
        String fechaComoCadena = (String) Tabla.getValueAt(fila, 4).toString();
        LocalDate fec = LocalDate.parse(fechaComoCadena);
        int cal = Integer.parseInt((String) Tabla.getValueAt(fila, 5).toString());
        
        txtId.setText(""+idc);
        txtNombre.setText(nom);
        txtGen.setText(gen);
        txtDuracion.setText(dur);
        txtFecha.setText(fec.toString());
        txtCalificacion.setText(Integer.toString(cal));
        }
    }//GEN-LAST:event_TablaMouseClicked

