private void buscarPelicula(String nombre) {
    DefaultTableModel modelo = (DefaultTableModel) Tabla.getModel();
    TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(modelo);
    Tabla.setRowSorter(tr);

    // Convierte el texto de búsqueda y los datos de la tabla a minúsculas
    tr.setRowFilter(RowFilter.regexFilter("(?i)" + nombre, 1)); // El modificador (?i) hace que la búsqueda sea insensible a mayúsculas y minúsculas en Java
    }//GEN-LAST:event_txtBuscarKeyReleased

   void Modificar(){
       String nom = txtNombre.getText();
       String gen = txtGen.getText();
       String dur = txtDuracion.getText();
       String fec = txtFecha.getText();
       String cal = txtCalificacion.getText();
       
               try{
            if(nom.equals("") || gen.equals("") || dur.equals("") || fec.equals("") || cal.equals("")) {
              JOptionPane.showMessageDialog(null,"Faltan datos");
              limpiarTabla();
            
        } else {
            String sql = "Update peliculas set id_pelicula ='"+idc+"', nombre='"+nom+"', genero='"+gen+"', duracion='"+dur+"', fecha='"+fec+"', calificacion='"+cal+"' where id_pelicula="+idc;
            
            conet = con1.getConnectionDB();
            st = conet.createStatement();
            st.executeUpdate(sql);
              JOptionPane.showMessageDialog(null,"Se modifico una pelicula");
              limpiarTabla();
            }
            
        }catch (Exception e){
            
        }
   }
    
